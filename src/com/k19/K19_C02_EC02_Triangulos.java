package com.k19;

public class K19_C02_EC02_Triangulos {

    public static void main(String[] args) {

        String caracter = "*";
        for (int i = 0; i < 5; i++) {
            System.out.println(caracter);
            caracter += "*";
        }
        caracter = "*";
        for (int j = 0; j < 5; j++) {
            System.out.println(caracter);
            caracter += "*";
        }
    }
}
