package com.k19;

public class K19_C02_C10_OperAritm {

    public static void main(String[] args) {

        int umMaisUm = 1 + 1;
        System.out.println("umMaisUm \t\t: " + umMaisUm);
        int tresVezesDois = 3 * 2;
        System.out.println("tresVezesDois \t\t: " + tresVezesDois);
        int quatroDivididoPor2 = 4 / 2;
        System.out.println("quatroDivididoPor2 \t: " + quatroDivididoPor2);
        int x = 7;
        System.out.println("int x = 7; \t\t " + x);
        x = x + 1 * 2;
        System.out.println("x = x + 1 * 2; \t\t " + x);
        x = x - 3;
        System.out.println("x = x - 3; \t\t " + x);
        x = x / (6 - (2 + (3 * 5) / (16 - 1)));
        System.out.println("x = x / (6 - 2 + (3 * 5) / (16 - 1)); " + x);

    }

}
